///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
#include "Scene_BasicCube.h"
#include "RenderObject_Cube.h"

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
Scene_BasicCube::Scene_BasicCube(float _width, float _height, Camera* _camera)
	:Scene(_width, _height, _camera)
{
	Init();
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
void Scene_BasicCube::Init()
{
	RenderObjectFactory_Cube* factory = new RenderObjectFactory_Cube();
	renderObjects.push_back(unique_ptr<RenderObject>(factory->CreateRenderObject("RenderObjectCube.vert", "RenderObjectCube.frag")));
	delete factory;

	glEnable(GL_DEPTH_TEST);
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
Scene* SceneFactory_BasicCube::CreateScene(float _width, float _height, Camera* _camera)
{
	return new Scene_BasicCube(_width, _height, _camera);
}
